//1부터 10까지 반복문
//10부터 1까지 반복문
using System;

namespace ConsoleApp4
{
    internal class Class2
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 10; i++)
            {
                Console.WriteLine(i);

            }


            for (int j = 10; j >= 0; j--)
            {
                Console.WriteLine(j);
            }

        }
    }
}
